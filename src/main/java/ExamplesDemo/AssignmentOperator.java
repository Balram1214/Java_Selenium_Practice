package ExamplesDemo;

public class AssignmentOperator {

	public static void main(String[] args) {
		// =, +=, -=, *=, /=, %=
		int n = 10;

		n += 2; // n = n+2
		System.out.println(n);

		n -= 4;
		System.out.println(n);

		n *= 3;
		System.out.println(n);

		n /= 2;
		System.out.println(n);

		n %= 5;
		System.out.println(n);

	}

}
