package ExamplesDemo;

public class EncapsulationDemo {

	int rollno = 123;
	String name = "Balram";

	public int getRollno() {
		return rollno;
	}

	public void setRollno(int r) {
		this.rollno = r;
	}

	public String getName() {
		return name;
	}

	public void setName(String n) {
		this.name = n;
	}

}
